import Foundation

func HZFont(fontSize: CGFloat) -> UIFont {
	return UIFont.systemFont(ofSize: fontSize)
}

func HZBFont(fontSize: CGFloat) -> UIFont {
	return UIFont.boldSystemFont(ofSize: fontSize)
}
